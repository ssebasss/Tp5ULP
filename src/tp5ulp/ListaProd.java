    private void BotonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAgregarActionPerformed
        // TODO add your handling code here:   
  String categoria = (String) jComboBoxCategorias.getSelectedItem();
    String nombre = jTextFieldNombre.getText();
    
    if (categoria == null) {
        String[] options = {"Volver a ingresar"};
        JOptionPane.showOptionDialog(this, "Debe seleccionar una categoria para continuar", "Advertencia",
                JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);
    } else if (nombre.isEmpty()) {
        String[] options = {"Volver a ingresar"};
        JOptionPane.showOptionDialog(this, "No puede ir nombre vac√≠o", "Advertencia",
                JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);
    } else {
        try {
            double precio = Double.parseDouble(jTextFieldPrecio.getText());
            Object[] arrObject = {nombre, categoria, precio};
            defaultTableModel.addRow(arrObject);

            jTextFieldNombre.setText("");
            jComboBoxCategorias.setSelectedIndex(-1); 
            jTextFieldPrecio.setText("");
        } catch (NumberFormatException e) {
            String[] options = {"Volver a ingresar"};
            JOptionPane.showOptionDialog(this, "El precio debe ser un n√∫mero", "Advertencia",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);
        }
    }
    }//GEN-LAST:event_BotonAgregarActionPerformed

